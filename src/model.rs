pub const MAX_TOKENS: usize = 32768;

#[derive(Clone)]
pub struct Model {
    model_type: ModelType,
    config: DynConfig,
    vb: VarBuilder<'static>,
    tokenizer: Tokenizer,
    vocab_size: usize,
    device: Device,
    seed: u64,
}

impl Model {
    pub fn new(model_type: ModelType, seed: u64, use_cuda: bool) -> Result<Self> {
        let (is_cuda, device) = if use_cuda && candle_core::utils::cuda_is_available() {
            (true, Device::new_cuda(0).unwrap())
        } else {
            (false, Device::Cpu)
        };

        let dtype = if is_cuda { DType::BF16 } else { DType::F32 };

        // Get the model repos
        let api = Api::new()?;
        let model_repo = model_type.model_repo();
        let tokenizer_repo = model_type.tokenizer_repo();

        // Create model config
        let config = model_type.create_config(&model_repo, &api);

        // Get the tokenizer and model files
        let tokenizer_filename = tokenizer_repo
            .file_paths(&[model_type.tokenizer_json_name()], &api)
            .pop()
            .unwrap();
        let model_filenames = model_type
            .model_names()
            .iter()
            .map(|name| model_repo.file_paths(&[name], &api).pop().unwrap())
            .collect::<Vec<_>>();

        // Create VarBuilder
        let vb = unsafe { VarBuilder::from_mmaped_safetensors(&model_filenames, dtype, &device)? };

        // Create tokenizer
        let tokenizer = Tokenizer::from_file(tokenizer_filename).map_err(E::msg)?;
        let vocab_size = tokenizer.get_vocab_size(false);

        Ok(Self {
            model_type,
            config,
            vb,
            tokenizer,
            device: device.clone(),
            vocab_size,
            seed,
        })
    }

    pub fn model_type(&self) -> &ModelType {
        &self.model_type
    }

    pub fn seed(&self) -> u64 {
        self.seed
    }

    pub fn set_seed(&mut self, seed: u64) {
        self.seed = seed;
    }

    pub fn max_tokens(&self) -> usize {
        MAX_TOKENS
    }

    pub fn new_token_string(&self) -> TokenString {
        TokenString::new(Vec::new(), self.clone())
    }

    pub fn tokenize_str(&self, text: impl Display) -> TokenString {
        // Tokenize the text
        let tokens = self.tokenizer.encode(text.to_string(), true).unwrap();

        // Get the token ids
        let token_ids = tokens.get_ids().to_vec();
        TokenString::new(token_ids, self.clone())
    }

    pub fn tokenize(&self, text: impl IntoTokenString) -> TokenString {
        text.into_token_string(self)
    }

    pub(crate) fn detokenize(&self, tokens: impl AsRef<[u32]>) -> String {
        // Decode the tokens into a string
        self
            .tokenizer
            .decode(tokens.as_ref(), true)
            .map_err(E::msg)
            .unwrap()
    }

    /// Attempt to get the token for a given string
    pub(crate) fn get_token(&self, s: impl Display) -> Result<u32> {
        match self.tokenizer.get_vocab(true).get(&s.to_string()) {
            Some(token) => Ok(*token),
            None => anyhow::bail!("cannot find the token for {:?}", s.to_string()),
        }
    }

    /// Get an iterator that yields tokens generated by the model.
    /// Returns an error if the prompt is empty.
    pub fn infer_iter(
        &self,
        prompt: impl IntoTokenString,
        seed: u64,
        temp: Option<f64>,
        top_p: Option<f64>,
        repeat_penalty: f32,
        repeat_last_n: usize,
    ) -> Result<InferIter> {
        // Add the model seed to the seed provided
        let seed = seed.wrapping_add(self.seed);

        // Tokenize the prompt
        let prompt = self.tokenize(prompt);

        // Fail if the prompt is empty
        if prompt.is_empty() {
            anyhow::bail!("prompt was empty")
        }

        // Create pipeline
        let pipeline = self
            .model_type
            .create_pipeline(&self.config, self.vb.clone());

        // Create logits processor
        let logits_processor = LogitsProcessor::new(seed, temp, top_p);

        // Get the end of text token
        let eos_tokens = (
            self.get_token("<|im_end|>").unwrap(),
            self.get_token("<|endoftext|>").unwrap(),
        );

        // Create the iterator
        Ok(InferIter::new(
            self.model_type.clone(),
            self.device.clone(),
            prompt,
            self.vocab_size,
            pipeline,
            logits_processor,
            repeat_penalty,
            repeat_last_n,
            eos_tokens,
        ))
    }

    /// Generate a response message based on the chat history.
    /// Returns an iterator over the generated response, and if thinking,
    /// then also returns the thoughts as a string.
    pub fn chat(
        &self,
        chat: &Chat,
        sender: &ChatRole,
        think: bool,
        seed: u64,
        temp: Option<f64>,
        top_p: Option<f64>,
        repeat_penalty: f32,
        repeat_last_n: usize,
    ) -> (InferIter, Option<String>) {
        const THINK_TEMP_MULTIPLIER: f64 = 0.85;

        // Panic if the model type does not support chat
        if !self.model_type.can_chat() {
            panic!("Model type {:?} does not support chat", self.model_type);
        }

        // If thinking, reduce the temperature a bit
        let temp = if think {
            temp.map(|t| t * THINK_TEMP_MULTIPLIER)
        } else {
            temp
        };

        // Create the basic chat prompt
        let mut prompt = self.model_type.create_chat_prompt(chat, sender, think);

        // If thinking then infer the contents of the think block and close it with </think>
        let mut thoughts = None;
        if think && self.model_type.can_think() {
            // First, infer the contents of the think block and store the result in `thoughts`
            thoughts = Some(
                self.infer_iter(&prompt, seed, temp, top_p, repeat_penalty, repeat_last_n)
                    .unwrap()
                    .complete(&["</think>"])
                    .0,
            );

            // Then append the thoughts to the prompt and close the think block
            prompt.push_str(thoughts.as_ref().unwrap());
            prompt.push_str("</think>");
        }

        // If we have a response prefix then append said prefix
        if let Some(prefix) = chat.response_prefix() {
            // Append the response prefix to the prompt
            prompt.push_str(prefix);
        }

        // Return the iterator over the generated response, as well as the thoughts (if any)
        (
            self.infer_iter(prompt, seed, temp, top_p, repeat_penalty, repeat_last_n)
                .unwrap(),
            thoughts,
        )
    }

    /// Instruct the model to do something, or generate/analyze text.
    /// Returns an iterator over the model's response to the instruction, and if thinking,
    /// then also returns the thoughts as a string.
    pub fn instruct(
        &self,
        instruction: impl Display,
        think: bool,
        seed: u64,
        temp: Option<f64>,
        top_p: Option<f64>,
        repeat_penalty: f32,
        repeat_last_n: usize,
    ) -> (InferIter, Option<String>) {
        // Give the model the instruction as a chat message
        let mut chat = Chat::new();
        chat.add_message(ChatRole::User, instruction.to_string());
        self.chat(
            &chat,
            &ChatRole::Model,
            think,
            seed,
            temp,
            top_p,
            repeat_penalty,
            repeat_last_n,
        )
    }

    /// Execute a pipeline on the model, returning the final context as an output JSON map.
    pub fn execute_pipeline(
        &self,
        pipeline: &crate::pipeline::Pipeline,
        input: impl Into<Map<String, JsonValue>>,
    ) -> Map<String, JsonValue> {
        pipeline.execute(self, input)
    }

    /// Predict the text which follows the given prompt.
    pub fn predict_next(
        &self,
        prompt: impl IntoTokenString,
        seed: u64,
        temp: Option<f64>,
        top_p: Option<f64>,
        repeat_penalty: f32,
        repeat_last_n: usize,
    ) -> InferIter {
        self.infer_iter(prompt, seed, temp, top_p, repeat_penalty, repeat_last_n)
            .unwrap()
    }

    /// Uses the model to several predictions in a chain, using the result of the previous
    /// prediction as part of the prompt for the next prediction.
    /// Use "{}" as placeholders in the template string where text should be generated.
    /// Returns all generated texts in order.
    /// Currently, the model does not look ahead when generating text for each placeholder,
    /// so it is best used for where you need a single word or idea for each placeholder.
    pub fn predict_chain(
        &self,
        template: impl AsRef<str>,
        seed: u64,
        temp: Option<f64>,
        top_p: Option<f64>,
        repeat_penalty: f32,
        repeat_last_n: usize,
    ) -> (Vec<String>, String) {
        let template = template.as_ref();
        let mut results = Vec::new();

        // Split the template into parts by the "{}" placeholders
        let parts: Vec<&str> = template.split("{}").collect();
        if parts.len() < 2 {
            panic!("Template must contain at least one '{{}}' placeholder");
        }

        // Generate text for each part except for the last
        let mut so_far = String::new();
        for (idx, part) in parts.iter().enumerate().take(parts.len() - 1) {
            let part = part.trim();
            let prompt = format!("{}{} **", so_far, part);
            let rest = self
                .predict_next(
                    prompt,
                    seed.wrapping_add(idx as u64),
                    temp,
                    top_p,
                    repeat_penalty,
                    repeat_last_n,
                )
                .complete(&["*", "\n"])
                .0
                .trim()
                .to_string();
            if idx > 0 {
                so_far.push(' ');
            }
            so_far.push_str(part);
            so_far.push(' ');
            so_far.push_str(&rest);
            results.push(rest);
        }

        // Append the last part without generating new text
        so_far.push_str(parts.last().unwrap());

        (results, so_far)
    }
}

/// Contains a pipeline, could be one of multiple types.
pub enum ModelPipeline {
    Qwen2(Qwen2),
    Qwen3(Qwen3),
    Qwen3Vl(Box<Qwen3Vl>),
}

impl ModelPipeline {
    pub fn forward(&mut self, xs: &Tensor, start_pos: usize, seq_len: usize) -> Tensor {
        match self {
            ModelPipeline::Qwen2(qwen2) => qwen2.forward(xs, start_pos).unwrap(),
            ModelPipeline::Qwen3(qwen3) => qwen3.forward(xs, start_pos).unwrap(),
            ModelPipeline::Qwen3Vl(qwen3_vl) => qwen3_vl
                .forward(
                    xs,
                    None,
                    None,
                    None,
                    None,
                    vec![seq_len],
                    vec![],
                    vec![],
                    &[start_pos],
                )
                .unwrap(),
        }
    }
}

/// Contains a model config
#[derive(Debug, Clone)]
pub enum DynConfig {
    Qwen2(Qwen2Config),
    Qwen3(Qwen3Config),
    Qwen3Vl(Qwen3VlConfig),
}

impl DynConfig {
    pub fn as_qwen2(&self) -> Option<&Qwen2Config> {
        match self {
            DynConfig::Qwen2(config) => Some(config),
            _ => None,
        }
    }

    pub fn as_qwen3(&self) -> Option<&Qwen3Config> {
        match self {
            DynConfig::Qwen3(config) => Some(config),
            _ => None,
        }
    }
    pub fn as_qwen3_vl(&self) -> Option<&Qwen3VlConfig> {
        match self {
            DynConfig::Qwen3Vl(config) => Some(config),
            _ => None,
        }
    }
}

/// Nudge a temperature value towards 1.0 without ever reaching it.
/// Useful for iterative tasks.
fn nudge_temperature(temp: &mut f64) {
    *temp += (1.0 - *temp) * 0.15;
}
